rm -rf /psa/tmp/backup.build 1>/dev/null 2>&1
rm -rf /psa/tmp/backup.list 1>/dev/null 2>&1
rm -rf /psa/tmp/backup.final 1>/dev/null 2>&1

docker ps --format '{{.Names}}' > /psa/tmp/backup.list
sed -i -e "/traefik/d" /psa/tmp/backup.list
sed -i -e "/watchtower/d" /psa/tmp/backup.list
sed -i -e "/wp-*/d" /psa/tmp/backup.list
sed -i -e "/x2go*/d" /psa/tmp/backup.list
sed -i -e "/psautomate/d" /psa/tmp/backup.list
sed -i -e "/cloudplow/d" /psa/tmp/backup.list
sed -i -e "/phlex/d" /psa/tmp/backup.list

#### Commenting Out To Let User See
num=0
while read p; do
let "num++"
echo -n $p >> /psa/tmp/backup.final
echo -n " " >> /psa/tmp/backup.final
  if [ "$num" == 7 ]; then
    num=0
    echo " " >> /psa/tmp/backup.final
  fi
done </psa/tmp/backup.list

running=$(cat /psa/tmp/backup.final)
# If Blank, Exit
if [ "$running" == "" ]; then
tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
⛔️ WARNING! - No Apps are Running! Exiting!
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
EOF
sleep 3
exit
fi

# Menu Interface
tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
🚀 PSABox - App Removal Interface
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

⚠️  Backup Data if Required! Removes Local App Data!

💾 Current Running Apps

$running

💬 Quitting? TYPE > exit
EOF
read -p '🌍 Type APP for QUEUE | Press [ENTER]: ' typed < /dev/tty

if [ "$typed" == "exit" ]; then exit; fi

tcheck=$(echo $running | grep "\<$typed\>")
if [ "$tcheck" == "" ]; then
  tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
⛔️  WARNING! - Type an Application Name! Case Senstive! Restarting!
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
EOF
sleep 3
bash /psa/psablitz/menu/removal/removal.sh
exit
fi

if [ "$typed" == "" ]; then
tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
⛔️  WARNING! - The App Name Cannot Be Blank!
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
EOF
sleep 3
bash /psa/traefik/tld.sh
exit
fi

tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
💎  PASS: UnInstalling - $typed
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
EOF
sleep 1.5

tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
🍖  NOM NOM - Stopping | Removing > $typed Docker Container
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
EOF
sleep .5

docker stop $typed 1>/dev/null 2>&1
docker rm $typed 1>/dev/null 2>&1

tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
🍖  NOM NOM - Removing /psa/data/$typed
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
EOF
sleep 1
rm -rf /psa/data/$typed

file="/psa/coreapps/apps/$typed.yml"
if [ -e "$file" ]; then
  check=$(cat /psa/coreapps/apps/$typed.yml | grep '##PSA-Community')
    if [ "$check" == "##PSA-Community" ]; then rm -r /psa/communityapps/apps/$typed.yml; fi
rm -rf /psa/var/community.app
fi

sleep 1.5

tee <<-EOF

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
✅️  PASS: Uninstalled - $typed - Exiting!
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
EOF
sleep 2
